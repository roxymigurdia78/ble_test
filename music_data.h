// Function to retrieve music data
// song_id: 0=DQ, 1=Star
// part_id: 1~N
Note* get_music_part(int song_id, int part_id) {
    switch(song_id) {
        case 0:
            switch(part_id) {
                case 1: return song_0_part_1;
                case 2: return song_0_part_2;
                case 3: return song_0_part_3;
                case 4: return song_0_part_4;
                case 5: return song_0_part_5;
                case 6: return song_0_part_6;
                default: return song_0_part_1;
            }
        case 1:
            switch(part_id) {
                case 1: return song_1_part_1;
                case 2: return song_1_part_2;
                case 3: return song_1_part_3;
                case 4: return song_1_part_4;
                case 5: return song_1_part_5;
                case 6: return song_1_part_6;
                case 7: return song_1_part_7;
                default: return song_1_part_1;
            }
        default: return NULL; // Invalid Song ID
    }
}

// 曲のBPM（テンポ）を返す関数（追加）
float get_song_bpm(int song_id) {
    switch(song_id) {
        case 0:
            return 120.0;
        case 1:
            return 100.0;
        default:
            return 120.0;
    }
}

